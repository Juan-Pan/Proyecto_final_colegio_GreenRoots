              <br>
              La madera presenta albura de color amarillo ocráceo, muy diferente del duramen de color
              castaño violáceo. Tiene brillo natural destacado, de textura fina, grano crespo, veteado
              pronunciado y decorativo, y es inodora. Se la clasifica como medianamente pesada y semi dura.
              El duramen es muy resistente al ataque de insectos xilófagos. La madera es tenaz, resistente al
              hendimiento, y se pule, lija y lustra con facilidad. Se usa en la elaboración de chapas finas,
              tornería, tallas, muebles de alta calidad, revestimientos de interiores, culatas de armas de fuego,
              instrumentos musicales, etc..
