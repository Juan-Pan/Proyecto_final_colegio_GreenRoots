              <br>
              El nogal común, nogal europeo o nogal español, y, en México, nuez de Castilla, es un árbol
              monoico y caducifolio (sus hojas se caen en invierno y otoño). Es la especie del género más
              difundida en Europa, extendiéndose por todo el suroeste y centro de Asia, hasta el
              Himalaya y el sudoeste de China.
              <br>
              Es cultivado por la madera, las hojas aromáticas y el fruto comestible, la nuez. Además
