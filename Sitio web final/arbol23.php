              <br>
              Es un árbol caducifolio que llega a los 25 m de altura con un tronco que puede superar los 2
              m de diámetro. De este tronco, que es corto y robusto y de color blanquecino o gris claro,
              salen gruesas y vigorosas ramas para formar una copa grande y redondeada. Las hojas, de
              20-35 cm de largo.

              <br>
              La juglona aludida anteriormente, que es abundante también en las raíces del nogal,
              produce alelopatía negativa vegetal, o sea que inhiben el crecimiento de otras plantas de su
              entorno para evitar la competencia. Todos los representantes de la familia Juglandaceae
              poseen, en mayor o menor grado, esta sustancia.

              <br>
              <b>hojas:</b> Las hojas contienen materias tánicas (Tanino), ácido elágico y ácido gálico.
              La semilla contiene hasta 50 % de aceite (predomina el ácido linoleico) y sacarosa,
              dextrosa, dextrina, fécula, pentosanas, la globulina yuglansina, lecitina, ácido
              inositopentafosfórico, etc.
