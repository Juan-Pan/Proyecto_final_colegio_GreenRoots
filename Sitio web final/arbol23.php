              <br>
              Es un fruto seco de alto valor nutritivo: rico en proteínas, vitaminas del grupo B, vitamina C,
              aceites vegetales, lecitina y ácidos grasos omega 3 (poliinsaturados).
              <br>
              Muchas plantaciones de nogal son de doble aprovechamiento, por su fruto comestible
              oleaginoso y su madera de gran calidad, similar a la del nogal negro (Juglans nigra), apreciada
