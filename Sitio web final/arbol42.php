              Los eucaliptos o eucaliptas, que significa ‘bien cubierto’, (refiriéndose a la semilla en su
              cápsula), es un género de árboles (y algunos arbustos). Existen alrededor de setecientas
              especies, la mayoría oriundas de Australia y Nueva Guinea. En la actualidad, se encuentran
              distribuidos por gran parte del mundo. Es considerada una especie exótica invasora en
              Colombia, Chile, Estados Unidos, Sudáfrica y algunos países de Europa.
              <br>
              Su principal utilización es en monocultivos forestales para la producción de pulpa de celulosa
              para la industria papelera. En menor medida, se utiliza como combustible, en la industria
              maderera o para la obtención de productos químicos, además de su valor ornamental.
              <br>
              Los eucaliptos son árboles y plantas medicinales perennes pirófitas, de porte recto. Pueden
              llegar a medir más de 60 m de altura, si bien se habla de ejemplares ya desaparecidos que han
              alcanzado los 150 m. En algunos ejemplares la corteza exterior (ritidoma) es marrón clara con
              aspecto de piel y se desprende a tiras dejando manchas grises sobre la corteza interior, más
              lisa.

              <br>
              La mayoría de los eucaliptos no toleran las heladas, o toleran ligeras heladas de hasta −3
              °C a −5 °C; los más resistentes al frío son los llamados «eucaliptos de nieve», el cual es
              capaz de resistir frío y heladas de hasta −20 °C. Algunas otras especies, especialmente de
              las altas mesetas y de las montañas del centro de Tasmania, han producido formas
              extremadamente resistentes al frío y se procuran semillas de esos linajes genéticos que se
              plantan para ornamento en áreas más frías del mundo.
              <br>
              <b>Hojas:</b> Las hojas jóvenes de los eucaliptos son ovaladas, grisáceas y de forma falciforme. Estas se
              alargan y se tornan de un color verde azulado brillante, de adultas, contienen un aceite
              esencial, de característico olor balsámico, que es un poderoso desinfectante natural.
            </font>
