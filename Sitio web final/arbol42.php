          <p class="card-text" align="justify">
            <font color="#f2f2f2" size="4px">
              La expedición de Cook a Australia en 1770 introdujo los eucaliptos al resto del mundo. Las
              primeras especies fueron recolectadas por Sir Joseph Banks, botánico de la expedición, que
              posteriormente las introdujo en lugares como California, el sur de Europa, África, Oriente
              Medio, el sur de Asia y América del Sur. Los primeros usos del eucaliptos fueron como
              cortavientos, ya que su madera es dura y difícil de aserrar.
              <br>
              El eucalipto es un árbol de crecimiento rápido, cuya raíz puede cortarse y volver a crecer. El
              principal beneficio de estos árboles es su madera, que se utiliza para la carpintería, para
              leña y para pulpa. Además, su madera presenta una gran resistencia a la descomposición
              por su alto contenido de aceite, por lo que se utiliza en postes de cercas. La celulosa puede
              extraerse para la producción de papel y biocombustibles.
              <br>
              El eucalipto es la fuente de fibra corta más común para la fabricación de pulpa de celulosa,
              utilizada en diversos productos de la industria papelera. Desde la década de los '70 que
              alrededor del 80% de los cultivos forestales de eucaliptos se destinan a este fin.
              <br>
              Según el tipo de pasta, se utilizan para la fabricación de cajas y cartones de embalaje, papel
              de periódico, etiquetas de productos, entre otros usos.
              <br>
              La longitud de la fibra de Eucalyptus es relativamente corta y uniforme con poca aspereza
              en comparación con otras maderas duras comúnmente utilizadas como madera para pulpa.
              <br>
              Las fibras son delgadas, pero de paredes relativamente gruesas. Esto proporciona una
              formación de papel uniforme y una alta opacidad que son importantes para todo tipo de
              papeles finos. La baja aspereza es importante para papeles estucados de alta calidad. El
              eucalipto es adecuado para muchos papeles tisú ya que las fibras cortas y delgadas dan
              una gran cantidad de fibras por gramo y la baja aspereza contribuye a la suavidad.
