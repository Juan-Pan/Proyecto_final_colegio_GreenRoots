              <br>
              La guanábana es en realidad un sincarpo, un agregado de frutos soldados, puede tener
              alrededor de 170 semillas, cada una corresponde a un fruto individual. El conjunto es el más
              grande de entre las anonáceas, mide de 14 a 40 cm de largo y 10 a 12 cm de diámetro, y
              pesa en promedio 2,9 kg, de los cuales 75,6 % corresponden a la pulpa, el 4,8 % es semilla,
