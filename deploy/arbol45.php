              Annona muricata, es una especie más cosmopolita, debido a que presentan un rango más
              amplio de adaptabilidad edáfica. La planta es muy sensible a los vientos.
              <br>
              <b>Ramas:</b> Las ramas son de color rojizo y sin vello, cilíndricas, arrugadas, ásperas y con numerosas
              lenticelas.
              <br>
              <b>Hojas:</b> Las hojas son simples, oblongo-elípticas a oblongo-obovadas enteras, duras, lisas, de color
              verde oscuro y 5-15 cm de longitud.
              <br>
              <b>Flores:</b> Las flores son las más grandes en su género, tienen un aroma penetrante y crecen sobre
              las ramas o tronco, son solitarias a lo largo del tallo, sépalos 3, ovados, menos de 5 mm de
              largo, pétalos 6, los 3 exteriores son ovados, libres, gruesos, de 2 a 3 cm de largo, los 3
              interiores, delgados y pequeños. Los 3 sépalos, ovado-elípticos a ovado-triangulares, miden
              3–5 mm. Los 6 pétalos son verdes y luego amarillentos, los 3 exteriores gruesos,
              ampliamente triangulares, de 2,5–5 por 2–4 cm, con el interior finamente pubescente y el
              ápice agudo a obtuso mientras los 3 internos son ovado-elípticos, de 2–4 por 1,5–3,5 cm,
              algo delgado, imbricados, pubescentes y con ápice obtuso.
              <br>
              <b>Semillas:</b> Las semillas son ovoides y aplanadas, de 15 a 20 mm de largo con testa oscura y brillante.
