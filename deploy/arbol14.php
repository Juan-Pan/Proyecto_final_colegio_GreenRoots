            <font color="#f2f2f2" size="4px">Es originario del subcontinente indio (India) y de la Indochina; introducida y cultivada en
              países de clima tropical además de algunos de clima templado o mediterráneo, por ejemplo
              en el sur de España (Provincia de Málaga).
              <br>
              En la zona intertropical es una planta sumamente noble: no requiere de riego y resiste los
              incendios; una plantación de mangos difícilmente podría quemarse durante la época de
              sequía, ya que es el período de máximo crecimiento de biomasa para estos árboles y de
              mayor actividad de la fotosíntesis por la menor nubosidad. Es un árbol agresivo con otras
              especies para ocupar un espacio determinado.
              <br>
              Es un árbol perenne (sus hojas permanecen verdes y funcionales durante más de una
              temporada de crecimiento) que puede alcanzar los 45 m de altura con una copa de 30 m de
              diámetro.
              <br>
              <b>Hojas:</b> Hojas alternas, simples, coriáceas, de lanceoladas a oblongas, de 15-30 cm de longitud, de
              color verde oscuro, inflorescencias en panículas axilares o terminales.
              <br>
              <b>Flores:</b> Sus flores poseen cinco pequeños sépalos verdes y peludos con cinco pétalos separados
              que son rojos, amarillos, anaranjados, rosados o verdes. En la base de los pétalos se
              encuentra un disco segmentado en cinco partes, el cual es carnoso y lleno de néctar.
              <br>
              <b>Fruto:</b> El fruto es una drupa (carnoso) que destaca entre sus principales características su buen
              sabor. Dicha pulpa puede ser o no fibrosa. Es una fruta normalmente de color verde en un
              principio, y amarillo, naranja e incluso rojo-granate cuando está madura, de sabor
              medianamente ácido cuando no ha madurado completamente.
            </font>
          </p>
