          </p>
          <p class="card-text" align="justify">
            <font color="#f2f2f2" size="4px">El mango, conocido esencialmente por su fruto, es también maderable cuando acaba su
              fase productiva de frutos. La madera puede verse afectada por hongos e insectos. A pesar
              de esta desventaja se utiliza para la fabricación de instrumentos como ukeleles, laminados y
              muebles baratos. Por otro lado, la madera contiene sustancias fenólicas que pueden
              producir dermatitis por contacto.
              <br>
              En Cuba se utiliza mucho para elaborar refrescos y preservarlos. Es el lugar con más
              variedades de mangos. Una de las zonas del mundo más conocidas por sus mangos es El
              Caney en la zona oriental de la isla (Santiago de Cuba).
              <br>
              En Colombia la comunidad indígena Siona del Putumayo prepara una infusión de las hojas
              y la corteza como desinfectante de heridas. Mientras que la comunidad Tikuna del
              Amazonas toma la decocción de las hojas como anticonceptivo durante los días sucesivos a
              la menstruación, también como abortivo tomando la decocción durante 3 días seguidos.
              <br>
              En varias regiones de Colombia se presentan usos tan diversos como: la decocción de las
              raíces se toma como diurético y para el tratamiento de enfermedades gastrointestinales.
              <br>
              Esta decocción también la usan para preparar tinturas para pigmentar la piel en
              enfermedades como el carate o vitíligo.
              <br>
              El masticar las hojas fortalece los dientes y actúa contra ampollas bucales; para esto
              también se usa la aplicación directa de la resina del árbol.
              <br>
              La infusión de la corteza se utiliza en el tratamiento del paludismo y los enjuagues en el
              cabello con esta decocción ayudan a oscurecer el cabello y evitar su caída.
              <br>
              En Perú, en la ciudad de Chulucanas, utilizan sus hojas para darle un color negativo y la
              brillantez a su famosa cerámica (queman las hojas cuando cuecen la cerámica).
              <br>
              En El Salvador, es una tradición que en la época de la Semana Santa se use en la
              gastronomía preparándolo con dulce de panela o con azúcares. Se le conoce como "Mango
              en Miel". También es muy popular comerlo cuando todavía está verde; se corta en mitades y
              se cubre con una mezcla de chile, sal y polvo de semilla de ayote (Alguashte).
