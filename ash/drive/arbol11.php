                </font></p>
                <p class="card-text" align="justify"><font color="#f2f2f2" size="4px">Las guaduas, cañazas o tacuaras son un género de plantas de la subfamilia del bambú.
                    <br>
                En el año de 1806 fue descrita por Alexander von Humboldt y Amadeo Bonpland quienes
                vieron esta planta en Colombia y la llamaron Bambusa guadua, luego en 1822 fue
                clasificada por Carl Sigismund Kunth como Guadua angustifolia.
                <br>
                Se considera como una de las plantas nativas más representativas de los bosques andinos.
                Desde las zonas tropicales de México hasta el sur en la Argentina, exceptuando Chile y las
                islas del Caribe, la guadua crece en todos los países de Iberoamérica y en buena parte de
                los países asiáticos.
                <br>
                Este recurso se utilizaba ya desde épocas remotas por parte de los primitivos pobladores de
                los Andes, y actualmente sigue siendo usada, especialmente en la región centro-occidental
                de Colombia.
                </font></p>
                <p class="card-text" ><font color="#f2f2f2" size="5px"><b>Usos:</b></font></p>
                <p class="card-text" align="justify"><font color="#f2f2f2" size="4px">
                Su uso es tan antiguo que, según el libro ‘Nuevas técnicas de construcción en bambú’
                (1978), en el Ecuador se han encontrado improntas de bambú en construcciones que se
                estima tienen 9500 años de antigüedad.
                <br>
                Puentes colgantes y atirantados de impresionante precisión de ingeniería, poderosas
                embarcaciones así como flautas, quenas y marimbas, fueron realizados por los incas con
                este recurso durante la época de preconquista, y después de ella durante la colonia.
                <br>
                Colombia, Ecuador y Panamá son los países de América que registran mayor tradición de
                uso, de hecho en estas zonas existieron las mayores extensiones de la especie en el
                continente.
                <br>
                En Colombia la guadua ha sido sometida a grandes presiones deforestadoras; de extensas
                áreas existentes ha pasado a pequeñas manchas boscosas ubicadas en las orillas de los
                ríos y en los bosques húmedos de las laderas de montaña, especialmente en los
                departamentos de Quindío, Risaralda, Caldas, Tolima, Valle del Cauca, Cundinamarca y
                Santander.
                <br>
                aporta grandes beneficios a la tierra y a las personas, pues con ella se puede construir casi
                todos los elementos de una casa. Es de muy rápido desarrollo, toma de 4 a 6 años para
                madurar y comienza su proceso de descomposición aproximadamente a los 10 años.
                <br>
                En la conquista española y, mucho después, la colonización antioqueña al viejo Caldas, la
                guadua fue un importante material empleado en la construcción de sus casas, de los
                utensilios caseros, herramientas de caza, ganadería y agricultura y hasta los acueductos.
                <br>
                Y Fernando Calzadilla Valdés, en su obra Por los Llanos de Apure señala que las lanzas
                que usaban los jinetes del ejército patriota en la campaña del Sur estaban elaborados con
                cañas de guadua, un material liviano y de gran dureza, que podía usarse como lanza en los
                combates para causar estragos entre la población realista.
                <br>
                Desde hace siglos la guadua ha sido utilizada tradicionalmente como material de
                construcción, y ahora, debido a la corriente actual de búsqueda de materiales para el
                desarrollo sostenible, esta planta ha ganado un espacio en la construcción.
                <br>
                Desde viviendas de zonas rurales construidas con bahareque en las cuales el armazón se
                construye con cañas de guadua, hasta para exportación en proyectos desarrollados en
                países donde está planta no existe. También se han construido casas con técnicas mixtas.
