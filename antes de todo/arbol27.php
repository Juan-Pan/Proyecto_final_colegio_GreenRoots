                <p class="card-text"><font color="#f2f2f2" size="5px">
                <b>Samán
                <br>
                Nombre cientifico:</b>
                Samanea saman
                <br>
                <b>Origen</b>
                Nativa
                </font></p>
                <p class="card-text" align="justify"><font color="#f2f2f2" size="4px">Samanea saman, también conocido como árbol de la lluvia, campano, cenízaro, cenícero,
                couji de Caracas, samán, tamarindo, es una especie botánica de árbol de hasta 20 m. Su
                etimología Samanea, es de su nombre nativo sudamericano saman, uno de los árboles
                emblemáticos de Venezuela.
                <br>
                Árbol con forma de un paraguas muy extenso, en paisajes urbanos, puede alcanzar los 60
                m en su área natural. Su copa llega a medir hasta 50 m o más de diámetro. Tiene hojas
                compuestas, bipinnadas de 3-9 pares de hasta 1 dm de largo, de 2-4 dm de longitud.
                
                <br>
                Es un árbol de crecimiento lento, sus raíces son superficiales y es de vida larga. Se
                reproduce por semilla, pero también se puede propagar a través de esquejes de tallo y raíz,
                aunque es difícil lograr su cultivo en zonas ganaderas por ser una planta forrajera (hojas y
                frutos) por lo que cuando son pequeños constituyen un alimento muy apetecible por el
                ganado y por lo tanto, vulnerables. Se ha dado el caso de que algunos samanes han
                logrado crecer espontáneamente en los Llanos venezolanos a partir de alguna semilla en la
                bosta del ganado.
                <br>
                De gran corpulencia, es una especie maderable muy importante, con una madera
                blanquecina, ligera, aunque dura. En los Llanos venezolanos y colombianos, los samanes
                no suelen presentar vegetación a su sombra, más por el hecho de que el ganado vacuno
                utiliza estos espacios como refugio que por otro motivo. A ello se añade el hecho de que la
                lluvia se escurre desde el centro de la copa hacia afuera, por lo que el agua que llega al
                suelo suele ser bastante escasa.

                <br>
                <b>Flores</b> Flores, de color rosado. Los frutos son legumbres o vainas oscuras de 8 a 20 cm de largo.
                Se puede encontrar con flores en los meses de octubre a abril, y con frutos de noviembre a
                marzo.
                </font></p>

                <p class="card-text" ><font color="#f2f2f2" size="5px"><b>Usos:</b></font></p>

                <p class="card-text" align="justify"><font color="#f2f2f2" size="4px">
                Presenta diverso usos, desde planta forrajera y maderable hasta como una buena fuente de
                alcohol absoluto y etanol.
                <br>
                En Venezuela y Colombia se usa en los estados ganaderos para dar sombra al ganado y
                como forrajera por sus legumbres verdes.
                <br>
                La madera se utiliza en ebanistería, construcción y, como leña y carbón de buena calidad
                así como para fabricar papel.
                <br>
                También se cultiva como ornamental. Es usualmente recomendado para la reforestación de
                zonas costeras degradadas, amenazadas por la erosión del suelo y la desertificación.
                <br>
                En algunas regiones el fruto se come, la pulpa pegajosa, dulce y con sabor a regaliz de las
                vainas de las semillas se comen crudas o se convierte en una bebida, y en tiempos de
                hambruna las hojas tiernas se comen en ensaladas sin embargo las semillas son tóxicas.
                <br>
                Debido a su prolífica floración, también es rentable para la producción de miel.
                <br>
                Las vainas de semillas molidas se usan para hacer biocombustibles, o se les da forma de
                bolas y se secan para obtener sustitutos de las pelotas de críquet.
                <br>
                En Tailandia el árbol se utiliza como hospedante de Laccifer lacca que se alimenta de la
                corteza del árbol, cubre las ramas con un pigmento resinoso rojizo y que es transformado
                posteriormente en goma laca. También produce una goma de inferior calidad usada como
                sustituto de la goma arábiga.
                <br>
                Las semillas secas son usadas para fabricar collares u otros objetos artesanales.
                </font></p>  

                <p class="card-text" ><font color="#f2f2f2" size="5px"><b>Usos medicinales:</b></font></p>

                <p class="card-text" align="justify"><font color="#f2f2f2" size="4px">
                En algunas zonas partes del árbol son usadas con supuestos fines medicinales, se
                mastican las semillas para tratar el dolor de garganta. De la infusión de hojas frescas y
                corteza interior se prepara té para tratar la diarrea. La cocción de corteza o raíz se utiliza en
                baños calientes para tratar el cáncer de estómago, sin embargo no hay evidencia que
                respalde estos usos.
                </font></p>  
